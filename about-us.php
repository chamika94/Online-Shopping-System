            <p class="text-justify">අලෙවි උපායමාර්ගයක් ලෙස එකවර වැඩිපුර සංඛ්‍යාවක් මිලට ගන්නා පාරිභෝගිකයකුට විශේෂ වට්ටම් ක්‍රමයක් ඔබට හඳුන්වා දිය හැකි ය. එහිදී ඔබට තනි පරිභෝගිකයින් කිහිප දෙනෙකුට භාණ්ඩය අලෙවි කිරීමට යන කාලය, පිරිවැය, ඉතිරිවන නිසා මිල අඩුකර වැඩි ඒකක ප්‍රමාණයක් මිලට ගන්නා පාරිභෝගිකයාට වට්ටමක් ලබාදීමෙන් ඔබට ද එකවර වැඩි ඒකක ප්‍රමාණයක් අලෙවි කරගැනීමටත් පාරිභෝගිකයාට ද ඉහළ තෘප්තියක් ලබාගැනීමටත් හැකිවේ. එම පාරි‍භෝගිකයා දිගින් දිගටම ඔබ සතුව තබාගැනීමට නම් වට්ටම් කාඩ්පතක් මුද්‍රණය කර තම වට්ටම් කාඩ්පත පාරිභෝගිකයාට ලබාදිය හැකි ය. එම කාඩ්පත ඉදිරිපත් කරන ඕනෑම පාරිභෝගිකයකුට කිසියම් මුදලක් ලකුණු කළ මිලෙන් අඩුකර භාණ්ඩය ලබාදිය හැකි ය. මෙවැනි නව අලෙවි උපක්‍රම භාවිතා කිරීම තුළින් ඔබගේ භාණ්ඩය පාරිභෝගිකයා අතර වඩාත් ජනප්‍රිය කළ හැකිය. දිනෙන් දින ඉල්ලුම වැඩිවන්නේ නම් එකවර අමුද්‍රව්‍ය විශාල ප්‍රමාණයක් සැපයුම් කරුවන්ගෙන් ලබාගත හැකි ය. එහිදී විශේෂ වට්ටමක් ලබා ගැනීමට ඔබට හැකිවේ. එසේ ලබාගන්නා වට්ටමේ වාසිය පාරිභෝගිකයාට ලබාදීමෙන් කලින් ඔබ විසින් ම ඔබගේ ලාභයෙන් කොටසක් වට්ටම් ලෙස ලබාදීම වලක්වා ගත හැකි ය. දැන් ඔබට පෙරට වඩා වැඩි අලෙවියක් ද වැඩි ලාභයක් ද ලබාගත හැකි ය. නිරන්තරයෙන් ම ව්‍යාපාරය දියුණු කර ගැනීමට අලුත් අලුත් ක්‍රියාමාර්ග ගැන සිතිය යුතු ය. බොහෝ ව්‍යාපාරිකයින් ව්‍යාපාර දිනෙන් දින දියුණු නොවී එකතැන වසර ගණනාවක් ම රැඳී සිටීමට ප්‍රධානතම කරුණ වන්නේ ඔවුන් නව ක්‍රියාමාර්ග ගැන නොසිතීම වේ. ඔබ සැමවිටම නව ක්‍රියාමාර්ග, නව අදහස් ක්‍රියාවට නැංවීමට අදිටන් කරගත යුතු ය.ඔබගේ ව්‍යාපාරය ලියා පදිංචි කිරීම ද කඩිනමින් සිදු කිරීම තුළින් ඔබට ණය පහසුකම් ලබාගැනීමට පහසුවේ. ඔබගේ නගරයට අලුත් තනි පුද්ගල ව්‍යාපාරයක් නිසා ග්‍රාම නිලධාරි වසමට අදාළ ප්‍රාදේශීය‍ ලේකම් කාර්යාලයෙන් අදාළ ආකෘති පත්‍ර පුරවා භාරදීමෙන් ව්‍යාපාරය ලියාපදිංචි කරගත හැකි ය. එහිදී ඔබට ලැබෙන ලියාපදිංචි සහතිකය ව්‍යාපාරික ස්ථානයේ පහසුවෙන් ප්‍රදර්ශනය වන පරිදි එල්ලා තිබිය යුතු ය.
